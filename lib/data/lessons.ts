/**
 * 問題のカテゴリとトピックからレッスンを取得
 * @param category カテゴリコード (e.g. "takkengyouhou")
 * @param topic トピック名 (e.g. "報酬額の制限")
 * @returns TopicLesson or undefined
 */
export function getLessonForQuestion(
  category: string,
  topic?: string
): TopicLesson | undefined {
  if (!topic) return undefined;
  const categoryLessons = allLessons[category];
  if (!categoryLessons) return undefined;
  const direct = categoryLessons[topic];
  if (direct) return direct;
  const aliased = topicAliases[topic];
  return aliased ? categoryLessons[aliased] : undefined;
}

/**
 * カテゴリ内の全レッスンを取得
 */
export function getLessonsForCategory(category: string): TopicLesson[] {
  const categoryLessons = allLessons[category];
  if (!categoryLessons) return [];
  // 重複除去（同一トピックの別名キーがあるため）
  const seen = new Set<string>();
  return Object.values(categoryLessons).filter((lesson) => {
    if (seen.has(lesson.topic)) return false;
    seen.add(lesson.topic);
    return true;
  });
}

