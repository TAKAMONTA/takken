/**
 * Step 2: 過去問傾向反映プロンプトビルダー
 * トピック別の傾向データをプロンプトに注入し、高品質な問題を生成
 */

import { TrendCategory, TrendTopic } from './trend-data';

/**
 * システムプロンプトを生成
 */
export function buildSystemPrompt(): string {
  return `あなたは宅地建物取引士試験（宅建試験）の問題作成の専門家です。
過去の出題傾向を分析し、傾向を反映したオリジナルの予想問題を作成します。

【絶対厳守ルール】
1. 実際の過去問を複製してはいけない。同じ法的概念をテストする場合でも、必ず新しいシナリオ・事例で作成すること
2. 問題文には具体的な人物名（A、B、C等）や数値を使い、実践的な事例問題にすること
3. 選択肢は4つ、正解は1つ。不正解の選択肢にも教育的な誤りを含めること
4. 解説は各選択肢について丁寧に説明し、関連する法律の条文・概念を明記すること
5. 出力は必ず指定のJSON形式のみ。余計な説明文は不要

【出力形式】
JSONの配列として出力してください。各問題は以下の形式:
{
  "question": "問題文",
  "options": ["選択肢1", "選択肢2", "選択肢3", "選択肢4"],
  "correctAnswer": 0,
  "explanation": "解説文"
}`;
}

/**
 * トピック別の問題生成プロンプトを構築
 */
export function buildTopicPrompt(
  category: TrendCategory,
  topic: TrendTopic,
  difficulty: '基礎' | '標準' | '応用',
  count: number
): string {
  const difficultyGuide = {
    '基礎': `基礎レベルの問題を作成してください。
- 条文や制度の基本的な知識を問う問題
- 「〜できるか」「〜は正しいか」のような直接的な問い
- 初学者でも取り組める明確な設問`,
    '標準': `標準レベル（実際の試験レベル）の問題を作成してください。
- 具体的な事例を交えた問題（「AがBに対して土地を売却した場合...」等）
- 原則と例外を問う問題
- 複数の条文の知識を組み合わせる問題`,
    '応用': `応用レベルの問題を作成してください。
- 複雑な事例を用いた総合問題
- 判例や通達レベルの知識を問う問題
- ひっかけではなく、深い理解を確認する良問`,
  }[difficulty];

  const questionPattern = category.id === 'takkengyouhou'
    ? '「宅地建物取引業法の規定によれば、次の記述のうち、正しいものはどれか。」'
    : category.id === 'kenri'
    ? '「民法の規定によれば、次の記述のうち、正しいものはどれか。」'
    : category.id === 'hourei'
    ? '「法令上の制限に関する次の記述のうち、正しいものはどれか。」'
    : '「税に関する次の記述のうち、正しいものはどれか。」';

  return `【カテゴリ】${category.name}
【トピック】${topic.name}
【出題頻度】${topic.frequencyDescription}（グレード${topic.grade}）
【このトピックの重要ポイント】
${topic.keyPoints.map((p, i) => `${i + 1}. ${p}`).join('\n')}

【難易度】${difficulty}
${difficultyGuide}

【出題パターンの参考】
${questionPattern}
※ 「正しいものはどれか」と「誤っているものはどれか」をバランスよく混ぜてください。

【生成する問題数】${count}問

【注意事項】
- 問題文は「${topic.name}に関する次の記述のうち〜」のパターンで始めること
- 上記の重要ポイントをカバーする問題を作成すること
- 実際の過去問をそのまま再現しないこと（同じ概念を新しい角度・シナリオで出題）
- 正解の番号は0〜3の間でランダムに分散させること
- 解説には「【正解】」「【各選択肢の解説】」「【ポイント】」を含めること

JSON配列のみを出力してください:`;
}
